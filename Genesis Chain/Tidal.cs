        public static bool ProccessTxtFileToLightFormat()
        {
            
            string filePath = Program._folderPath + "tidals.txt";
            string[] s = File.ReadAllLines(filePath);
            // first get the date 
            if (s[0].Contains("Usage:")) { return false;  }
            string datetimeLine = s[4];
            string[] parser = datetimeLine.Split(' ');
            string dateString = parser[6];
            parser = dateString.Split('-'); // annee - mois - jour
            uint year = uint.Parse(parser[0]);
            uint month = uint.Parse(parser[1]);
            uint day = uint.Parse(parser[2]);
            // now we have the stuff .. we will do some float here 
            List<float> value = new List<float>();
            List<byte[]> date = new List<byte[]>(); // just a list of 2 bytes . 
            for (int i = 4; i < s.Length - 1; i ++)
            {
                parser = s[i].Split(' ');
                if ( parser.Length < 2)
                {
                    // no proccess no line found
                }
                else
                {
                    // proccess all the shit
                    // i need to replace the . into , // just check if it is the same for linux ... 
                    value.Add(float.Parse(parser[2].Replace('.', ',')));

                    // first get the water ... then work with c_arr ... 
                    string cleaned = s[i].Replace(" ", "");
                    //Console.WriteLine(cleaned);
                    //Console.WriteLine("no parse : " + s[i]);
                    char[] c_arr = cleaned.ToCharArray();
                    // its char 6 or char 7 ... it depends ... 
                    uint length = 4;
                    if (c_arr[9] == 'U') { length = 5; } // probleme here
                    string date_str = "";
                    if (length == 4)
                    {
                        // start at c_arr idnex 7 
                        for (int n = 4; n < 4 + length; n++)
                        {
                            date_str += c_arr[n].ToString();
                        }
                    }
                    else
                    {
                        for (int n = 4; n < 4 + length; n++)
                        {
                            date_str += c_arr[n].ToString();
                        }
                        // start at c_arr idnex 6 
                    }
                    //Console.WriteLine("number of minutes in file : " + value.Count);
                    parser = date_str.Split(':');
                    byte hour = byte.Parse(parser[0]);
                    byte minute = byte.Parse(parser[1]);
                    // should convert the datee in utc format ... 
                    byte[] lightdateformat = new byte[2] { hour, minute };
                    date.Add(lightdateformat);
                }
                
          
            }
            // just all the stuff into light format ... 
            List<byte> DataBuider = new List<byte>();
            DataBuider = Program.AddBytesToList(DataBuider, BitConverter.GetBytes(year));
            DataBuider = Program.AddBytesToList(DataBuider, BitConverter.GetBytes(month));
            DataBuider = Program.AddBytesToList(DataBuider, BitConverter.GetBytes(day));
            for (int i = 0; i < value.Count; i ++)
            {
                DataBuider = Program.AddBytesToList(DataBuider, BitConverter.GetBytes(value[i]));
                DataBuider = Program.AddBytesToList(DataBuider, date[i]);
            }
            File.WriteAllBytes(Program._folderPath + "tidaloracle", Program.ListToByteArray(DataBuider));
            File.Delete(filePath);
            return true;
        }

        public static float GetTidalAtSpecificTime(uint timestamp)
        {
           
            DateTime dt = new DateTime(1970, 1, 1).AddSeconds(timestamp);

            TimeSpan ts = dt.Subtract(LATEST_DAY_START_REQUEST);
            // there is a possibility that it is negative number if it is an old block timestamp so then we need to recompute ... 

            int cycle = (int)Math.Floor(ts.TotalMinutes);
          //  Console.WriteLine("time spent : " + cycle + "minutes ");
            if ( cycle < 0 )
            {
             //   Console.WriteLine("downgrading tidal value ");
                if (!GetTidalPrevision(timestamp)) // specify a custom start based on the timestamp ... 
                {
                //    Console.WriteLine("attempting to recreate tidal prev but failed "); Console.ReadKey();
                    Environment.Exit(0); return 0;
                }
                if (!SetLatestRequest())
                {
                //    Console.WriteLine("attempting to recreate tidal prev but failed "); Console.ReadKey();
                    Environment.Exit(0); return 0;
                };
            }
            if ( cycle > 142500)
            {
                // time to process new tidal ;)
                if (!GetTidalPrevision()) {// Console.WriteLine("attempting to recreate tidal prev but failed ");
                    Console.ReadKey() ;
                    Environment.Exit(0); return 0; }
                if (!SetLatestRequest()) { //Console.WriteLine("attempting to recreate tidal prev but failed ");
                    Console.ReadKey();  
                    Environment.Exit(0); return 0; };
            }
        
            uint byteOffset = 12 + (uint)cycle * 6;
            byteOffset -= (TIDAL_TIME_OFFSET * 6 ) ; // just adjust ( temp soluce ... )
           // Console.WriteLine("byteOffset is " + byteOffset);
            byte[] readBytes = Program.GetBytesFromFile(byteOffset, 6, Program._folderPath + "tidaloracle");
            float waterlevel = BitConverter.ToSingle(new byte[4] { readBytes[0], readBytes[1], readBytes[2], readBytes[3] }, 0);
            byte hour = readBytes[4];
            byte minute = readBytes[5];
           // Console.WriteLine("result at time " + hour + ":" + minute + " -> " + waterlevel);
            return waterlevel;
        }

